def policyIteration(policy, target, U):
    print("During the policy iteration:\n")
    while True:
        U = policyEvaluation(policy, U, target)
        unchanged = True
        for r in range(NUM_ROW):
            for c in range(NUM_COL):
                if (r == target[0] and c == target[1]) or (U[r][c] == -1):
                    continue
                maxAction, maxU = None, -float("inf")
                for action in range(NUM_ACTIONS):
                    u = calculateU(U, r, c, action)
                    if u > maxU:
                        maxAction, maxU = action, u
                if maxU > calculateU(U, r, c, policy[r][c]):
                    policy[r][c] = maxAction # the action that maximizes the utility
                    unchanged = False
        if unchanged:
            break
        printEnvironment(policy, target)
    return policy


